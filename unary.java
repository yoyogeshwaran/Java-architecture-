package operator;

public class unary {

	public static void main(String[] args) {
		int a=10;
		int b=-11;
		System.out.println(a);
		int result1=a++;
		int result2=++a;
		System.out.println(result1);
		System.out.println(result2);
		int result3=--a;
		System.out.println(result3);
		int result4=a--;
		System.out.println(result4);
		int result5=~b;
		System.out.println(result5);

		
	}

}
